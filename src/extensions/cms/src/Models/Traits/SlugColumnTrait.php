namespace Autumn\Extensions\Cms\Models\Traits;

use Autumn\Database\Attributes\Column;

trait SlugColumnTrait
{
    #[Column(type: Column::TYPE_STRING, name: 'slug', size: 200, charset: Column::CHARSET_UTF8)]
    private string $slug = '';

    /**
     * @return string
     */
    public function getSlug(): string
    {
        return $this->slug;
    }

    /**
     * @param string $slug
     */
    public function setSlug(string $slug): void
    {
        $this->slug = $slug;
    }
}